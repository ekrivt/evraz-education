package jquery.datatables.model;

public class AppointmentTransaction {
	
	
	private int id;
	private String appointmentdate;
	private String pid;
	private String created_date;
	private String updateddate;
	private String ccoid;
	private String slotid;
	private String availableslots;
	private String TotalSlots;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the appointmentdate
	 */
	public String getAppointmentdate() {
		return appointmentdate;
	}
	/**
	 * @param appointmentdate the appointmentdate to set
	 */
	public void setAppointmentdate(String appointmentdate) {
		this.appointmentdate = appointmentdate;
	}
	/**
	 * @return the pid
	 */
	public String getPid() {
		return pid;
	}
	/**
	 * @param pid the pid to set
	 */
	public void setPid(String pid) {
		this.pid = pid;
	}
	/**
	 * @return the created_date
	 */
	public String getCreated_date() {
		return created_date;
	}
	/**
	 * @param created_date the created_date to set
	 */
	public void setCreated_date(String created_date) {
		this.created_date = created_date;
	}
	/**
	 * @return the updateddate
	 */
	public String getUpdateddate() {
		return updateddate;
	}
	/**
	 * @param updateddate the updateddate to set
	 */
	public void setUpdateddate(String updateddate) {
		this.updateddate = updateddate;
	}
	/**
	 * @return the ccoid
	 */
	public String getCcoid() {
		return ccoid;
	}
	/**
	 * @param ccoid the ccoid to set
	 */
	public void setCcoid(String ccoid) {
		this.ccoid = ccoid;
	}
	/**
	 * @return the slotid
	 */
	public String getSlotid() {
		return slotid;
	}
	/**
	 * @param slotid the slotid to set
	 */
	public void setSlotid(String slotid) {
		this.slotid = slotid;
	}
	/**
	 * @return the availableslots
	 */
	public String getAvailableslots() {
		return availableslots;
	}
	/**
	 * @param availableslots the availableslots to set
	 */
	public void setAvailableslots(String availableslots) {
		this.availableslots = availableslots;
	}
	/**
	 * @return the totalSlots
	 */
	public String getTotalSlots() {
		return TotalSlots;
	}
	/**
	 * @param totalSlots the totalSlots to set
	 */
	public void setTotalSlots(String totalSlots) {
		TotalSlots = totalSlots;
	}
	
	

}
